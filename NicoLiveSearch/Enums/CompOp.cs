
namespace NicoLiveSearch
{
    ///<summary>フィルター比較方法 (comparison operator)</summary>
    public enum CompOp
    {
        ///<summary>＝ 等しい (equal)</summary>
        EQ,

        ///<summary>＞ 超 (greater than)</summary>
        GT,

        ///<summary>≧ 以上 (greater than or equal to)</summary>
        GTE,

        ///<summary>≦ 以下 (less than or equal to)</summary>
        LTE,

        ///<summary>＜ 未満 (less than)</summary>
        LT,
    }
}
